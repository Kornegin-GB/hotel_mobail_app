class CounterTourist {
  String getTitleTourist(int index) {
    switch (index + 1) {
      case 1:
        return "Первый";
      case 2:
        return "Второй";
      case 3:
        return "Третий";
      case 4:
        return "Четвертый";
      case 5:
        return "Пятый";
      case 6:
        return "Шестой";
      case 7:
        return "Седьмой";
      case 8:
        return "Восьмой";
      default:
    }
    return "";
  }
}
